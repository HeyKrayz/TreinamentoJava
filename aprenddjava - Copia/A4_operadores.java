public class A4_operadores {
    public static void main (String [] args) {

// numeros q vou testar

        double number_1 = 19;
        double number_2 = 11;
        double number_3 = 16;

// usando operadores

        double soma = number_1 + number_2;
        double subtraçao = number_3 - number_1;
        double multiplicaçao = number_2 * number_2;
        double divisao = number_1 / number_2;

        System.out.println ("a soma de 19+11 =  "+ soma);
        System.out.println ("16-19 = " + subtraçao);
        System.out.println ("11x11 = "+ multiplicaçao);
        System.out.println ("19 dividido por 11 = " + divisao);

    }
}